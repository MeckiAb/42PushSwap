#include <unistd.h>

int	print_nbr(int n, int *i)
{
	char	c;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		*i += 11;
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		*i = print_nbr(-n, i) + 1;
	}
	else if (n > 9)
	{
		*i = print_nbr(n / 10, i) + 1;
		c = n % 10 + '0';
		write(1, &c, 1);
	}
	else
	{
		c = n % 10 + '0';
		write(1, &c, 1);
		*i = *i + 1;
	}
	return (*i);
}
