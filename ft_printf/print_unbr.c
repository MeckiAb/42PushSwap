#include <unistd.h>

int	print_unbr(unsigned int n, int *i)
{
	char	c;

	if (n > 9)
	{
		*i = print_unbr(n / 10, i) + 1;
		c = n % 10 + '0';
		write(1, &c, 1);
	}
	else
	{
		c = n % 10 + '0';
		write(1, &c, 1);
		*i = *i + 1;
	}
	return (*i);
}
